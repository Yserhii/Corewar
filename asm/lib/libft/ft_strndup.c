#include "libft.h"

char	*ft_strndup(const char *s1, int n)
{
	int		i;
	char	*s2;

	i = 0;
	if ((s2 = ft_memalloc(n + 1)) != NULL)
	{
		while (s1[i] && i < n)
		{
			s2[i] = s1[i];
			i++;
		}
		s2[i] = '\0';
		return (s2);
	}
	return (NULL);
}
