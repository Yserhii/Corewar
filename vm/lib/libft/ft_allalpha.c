#include "libft.h"

int		ft_allalpha(char *str)
{
	if (!*str)
		return (1);
	while ((*str >= 'a' && 'z' <= *str) || (*str >= 'A' && 'Z' <= *str))
		str++;
	if (*str)
		return (0);
	else
		return (1);
}
